-- Create the 'bookings' table for Hotel/Daycare
create table bookings (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  pet_id bigint references pets(id),
  pet_name text, -- Storing name for easier display, or could join tables
  service_type text, -- 'Hotel' or 'Daycare'
  task_description text,
  status text default 'checkIn' -- 'checkIn', 'playtime', 'napTime', 'grooming', 'ready'
);

