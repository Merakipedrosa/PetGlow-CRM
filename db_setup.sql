-- Create the 'pets' table
create table pets (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  name text not null,
  breed text,
  age text,
  photo_url text,
  cuteness_level integer default 100,
  badges text[], -- Array of strings for badges like ['Friendly', 'Napper']
  status text default 'Active' -- 'Active', 'Checked In', etc.
);

